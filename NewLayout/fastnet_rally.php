			<span class="newstitle">Getting into gear for Westlodge Hotel Fastnet Rally - Press Release No. 1</span><span class="newsdate">1st July 2015</span>
			<p class="setmargin">
			The Skibbereen & District Car Club are currently in the midst of organising their premier event, the Westlodge Hotel Fastnet Rally that takes place on Sunday 25th October next.  The rally is the final round of the Triton Showers National Rally Championship and also rounds of the PlasticBags.ie Southern 4 Rally Championship and the Carbery Plastics Skibbereen & District Car Club Championship.
			</p>
			
			<p class="setmargin">
			Clerk of the Course for the nine stage event Ger Hayes commented on changes to different aspects of the rally, “We will have the Ceremonial Start on Saturday evening (24th October) probably a little earlier than other years.  There will be a brief service on Sunday morning, following which the rally will be based on a three stage loop format with servicing after the first and second loops. I am very pleased with the route chosen this year and I think competitors and spectators will have an impressive day of rallying. The Westlodge Hotel is again our title sponsor and rally headquarters.  We intend to have the finish ramp outside the main entrance of the hotel. The prize giving ceremony will also take place much earlier than previous years, probably around 8pm”
			</p>

			<p class="setmargin">
			Meanwhile, Pat Gill, General Manager of the Westlodge Hotel remarked “I am delighted that the Westlodge Hotel is the title sponsor yet again and I look forward to being associated with the rally for as long as possible for both the Westlodge Hotel and for the town of Bantry. I look forward to welcoming all competitors and spectators from far and wide to what promises to be a fantastic weekend. We want to offer more to the rallying fraternity and also improve the property here, we want more people to stay here over the two nights rather than one night. We also want to put on more entertainment for families.  We will offer a great range of deals for the rally weekend.
			</p>
			
			<p class="setmargin"> 
			Eileen O’Shea, Chairperson of the Bantry Development and Tourism Association commented, “We run five different festivals here in Bantry and the Westlodge Hotel Fastnet Rally is one of the main events of the season. We are looking forward to having it back again this year. It’s proving very successful due to the great support we get here in the town and the publicity that it generates.
			</p>
			
			<p class="setmargin">
			The partnership between the Skibbereen and District Car Club and the Bantry Development and Tourism Association continues to be a vital part of the rally and both groups are committed to raising the profile of the maritime town. Eileen added, “Yes, I think it is doing that, and in fact it can get better, we are working on it to attract more people, especially overseas competitors. Tourism is all about getting visitors into the town, we want to attract more drivers in the hope that they will come back for the family holidays.”
			On the Limit Sports” will be in Bantry to record the action as it happens.  The rally will be televised on terrestrial and satellite television which will prove to be a huge boost for the tourism industry.
			</p>
			
			<p class="setmargin">
			Entries open on September 14th next and regulations, when approved by Motorsport Ireland, will be
			available to download from www.skibbdcc.com.
			</p>
			
